'use strict';
let number1 = 5;         // Definition einer Ganzzahl
let number2 = 0.5;       // Definition einer Kommazahl
let number3 = -22;       // Definition einer negativen Ganzzahl
let number4 = -0.9;      // Definition einer negativen Kommazahl
let number5 = 12_300;    // Trennzeichen bei Dezimaldarstellung
let number6 = 1_000_000; // Trennzeichen bei Dezimaldarstellung

console.log(number1);    // 5
console.log(number2);    // 0.5
console.log(number3);    // -22
console.log(number4);    // -0-9
console.log(number5);    // 12300
console.log(number6);    // 1000000
